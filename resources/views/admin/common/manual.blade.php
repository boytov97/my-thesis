            <h5>Виджеттер</h5>
            <p>Виджеттер модулу сайттагы эч кандай атайын запростун жардамысыз жүктөлүүчү маалыматтардын тизмесин жана
                аларды иштетүүчү функционалдарды камтыйт. Андагы сакталган маалыматтарды жалпысынан "виджеттер" деп атоо
                шартка жараша. Бул модуль виджеттерди түзүп, сактап, оңдоп жана өчүрүүчү функияларга ээ. Адистер тарабынан
                түзүлгөн виджеттерди өчүрбөө сунушталат. Ал эми виджеттер модулу төмөнкү талаалардан турат:
                <ul style="margin-left: 30px;">
                    <li>Аталышы</li>
                    <li>Адрестик аты</li>
                    <li>Жарыялоо</li>
                    <li>Мазмундук текст</li>
                </ul>
            </p>

            <h5>Сүрөттөр</h5>
            <p>Сүрөттөр модулу сайттагы грамматика тууралуу маалыматтарды камтыган сүрөттөрдүн тизмесин жана
                аларды иштетүүчү функционалдарды камтыйт. Бул модуль грамматика тууралуу маалыматтарды камтыган сүрөттөрдү
                сактап, оңдоп жана өчүрүүчү функияларга ээ. Ал эми сүрөттөр модулу
                төмөнкү талаалардан турат:
                <ul style="margin-left: 30px;">
                    <li>Аталышы</li>
                    <li>Категроия</li>
                    <li>Сүрөт</li>
                    <li>Жарыялоо</li>
                    <li>Башкы баракчага</li>
                </ul>
            
