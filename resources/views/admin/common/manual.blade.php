            жарыялайт.
            </p>

            <h5>Дареги</h5>
            <p>Дареги модулу мекеменин дарегин, дареги баракчасында жарялоонучу Google картанын берилиштерин сактайт.
               Бул маалыматтарды бир эле жолу сактап жана ошону өзгөртүү туура иш. Эки же андан көп берилиштер сакталган
               учурда алардын эң акыркы сакталган гана колдонулат. Модулдун формасы төмөнкү талааларды камтыйт:
                <ul style="margin-left: 30px;">
                    <li>Орточо жакындак</li>
                    <li>Кеңдик</li>
                    <li>Алыстык</li>
                    <li>Адрес</li>
                </ul>

            "Орточо жакындык" талаасына көрсөтүлгөн берилиш спутниктен жерге чейинки аралыкты көрсөтүүчү берилиш катары
            каралат. "Кеңдик" жана "Алыстык" талааларына берүүлүүчү берилиштерди Google картадан керектүү жерди белгилеп
            алып алсак болот.
