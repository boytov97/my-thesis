            <h5>Слайдер</h5>
            <p>Слайдер модулу сайттын башкы баракчасындагы слайдерди түзүүчү сүрөттөрдүн тизмесин жана
                аларды иштетүүчү функционалдарды камтыйт. Бул модуль слайттарды
                сактап, оңдоп жана өчүрүүчү функияларга ээ. Ал эми слайдер модулу
                төмөнкү талаалардан турат:
                <ul style="margin-left: 30px;">
