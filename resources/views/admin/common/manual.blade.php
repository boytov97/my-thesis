        </div>
        <div class="manual__content">
            <p>Бул модул проекттин администраторлор панелине достубу бар колдонуучулар тизмесин көрсөтүп жана алардын
                кызмат ордун, статусун алмаштыруу, мүмкүнчүлүктөрүн чектеп же арттыруу, өчүрүү функционалын аткарат. Бирок,
                кызмат ордун, статусун алмаштыруу, мүмкүнчүлүктөрүн чектеп же арттыруу, өчүрүү функционалы менен бир гана
                "супер-админ" статусундага администраторлор колдоно алат. Эгерде "супер-админ" статусуна ээ эмес администратор
                өзгөртүү аракетинде оңдоо баракчасына ачууга аракет кылса, ал автоматтык түрдө кайра "администраторлор тизмеси"
                баракчасына жөнөтүлөт. Демек, супер-админден башкасы оңдоо баракчасына доступ ала албайт.
            </p>
